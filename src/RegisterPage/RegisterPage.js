import React, { useState, useEffect } from 'react';
import { Link } from 'react-router-dom';
import { useDispatch, useSelector } from 'react-redux';

import { userActions } from '../_actions';

function RegisterPage() {
    const [user, setUser] = useState({
        CustomerName: '',
        CustomerSurname: '',
        Email: '',
        Password: ''
    });
    const [submitted, setSubmitted] = useState(false);
    const registering = useSelector(state => state.registration.registering);
    const dispatch = useDispatch();

    // reset login status
    useEffect(() => {
        dispatch(userActions.logout());
    }, []);

    function handleChange(e) {
        const { name, value } = e.target;
        setUser(user => ({ ...user, [name]: value }));
    }

    function handleSubmit(e) {
        e.preventDefault();

        setSubmitted(true);
        if (user.CustomerName && user.CustomerSurname && user.Email && user.Password) {
            dispatch(userActions.register(user));
        }
    }

    return (
        <div className="col-lg-8 offset-lg-2">
            <h2>Register</h2>
            <form name="form" onSubmit={handleSubmit}>
                <div className="form-group">
                    <label>First Name</label>
                    <input type="text" name="CustomerName" value={user.CustomerName} onChange={handleChange} className={'form-control' + (submitted && !user.CustomerName ? ' is-invalid' : '')} />
                    {submitted && !user.CustomerName &&
                        <div className="invalid-feedback">First Name is required</div>
                    }
                </div>
                <div className="form-group">
                    <label>Last Name</label>
                    <input type="text" name="CustomerSurname" value={user.CustomerSurname} onChange={handleChange} className={'form-control' + (submitted && !user.CustomerSurname ? ' is-invalid' : '')} />
                    {submitted && !user.CustomerSurname &&
                        <div className="invalid-feedback">Last Name is required</div>
                    }
                </div>
                <div className="form-group">
                    <label>Email</label>
                    <input type="text" name="Email" value={user.Email} onChange={handleChange} className={'form-control' + (submitted && !user.Email ? ' is-invalid' : '')} />
                    {submitted && !user.Email &&
                        <div className="invalid-feedback">Email is required</div>
                    }
                </div>
                <div className="form-group">
                    <label>Password</label>
                    <input type="Password" name="Password" value={user.Password} onChange={handleChange} className={'form-control' + (submitted && !user.Password ? ' is-invalid' : '')} />
                    {submitted && !user.Password &&
                        <div className="invalid-feedback">Password is required</div>
                    }
                </div>
                <div className="form-group">
                    <button className="btn btn-primary">
                        {registering && <span className="spinner-border spinner-border-sm mr-1"></span>}
                        Register
                    </button>
                    <Link to="/login" className="btn btn-link">Cancel</Link>
                </div>
            </form>
        </div>
    );
}

export { RegisterPage }